
public class TipoDeDatos {
	public static void  main(String[] arg) {
		int numero = 54;
		byte numerosPequenios = 100;
		long numeroGrande = 1000000000;
		float numeroDecimal = 123.3432f;
		double numeroDecimalGrande = 123123;
		boolean verdadero = true; 
		boolean falso = false;
		char letra = 'f';
		
		// Dato Estructurado
		
		String nombre = "Juseppe";
		
		System.out.println(numero);
		System.out.println(numerosPequenios);
		System.out.println(numeroGrande);
		System.out.println(numeroDecimalGrande);
		System.out.println(verdadero);
		System.out.println(falso);
		System.out.println(letra);
		System.out.println(nombre);
	}
}
